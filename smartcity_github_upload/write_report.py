BLUE = RGBColor(26, 79, 139)
GRAY = RGBColor(100, 100, 100)
DARK = RGBColor(30, 30, 30)

def set_kor(run):
    rpr = run._element.get_or_add_rPr()
    rf = rpr.find(qn("w:rFonts"))
    if rf is None:
        rf = OxmlElement("w:rFonts")
        rpr.insert(0, rf)
    rf.set(qn("w:eastAsia"), "맑은 고딕")

def sf(run, bold=False, size=10, color=None, italic=False):
    run.bold   = bold
    run.italic = italic
    run.font.size = Pt(size)
    run.font.name = "맑은 고딕"
    set_kor(run)
    if color:
        run.font.color.rgb = color

def h(level, text):
    p = doc.add_heading(text, level=level)
    for run in p.runs:
        run.font.name = "맑은 고딕"
        set_kor(run)
        if level == 1:
            run.font.size = Pt(16); run.font.color.rgb = BLUE
        elif level == 2:
            run.font.size = Pt(13); run.font.color.rgb = BLUE
        elif level == 3:
            run.font.size = Pt(11); run.font.color.rgb = RGBColor(50,50,50)
    return p

def body(text, size=10):
    p = doc.add_paragraph()
    r = p.add_run(text)
    sf(r, size=size, color=DARK)
    return p

def note(text, size=9):
    p = doc.add_paragraph()
    r = p.add_run(text)
    sf(r, size=size, color=GRAY)
    return p

def toc_entry(title, page, size=11):
    p = doc.add_paragraph()
    tab_stops = p.paragraph_format.tab_stops
    tab_stops.add_tab_stop(Cm(15.5), WD_TAB_ALIGNMENT.RIGHT, WD_TAB_LEADER.DOTS)
    r = p.add_run(f"{title}\t{page}")
    sf(r, size=size, color=DARK)
    return p

# footnote-style inline citation box
def cite_box(num, ref_text):
    """Small indented citation paragraph"""
    p = doc.add_paragraph()
    p.paragraph_format.left_indent = Cm(0.8)
    r1 = p.add_run(f"[{num}] ")
    sf(r1, bold=True, size=8.5, color=BLUE)
    r2 = p.add_run(ref_text)
    sf(r2, size=8.5, color=GRAY)
    return p

def shd_cell(cell, fill):
    s = OxmlElement("w:shd")
    s.set(qn("w:fill"), fill)
    s.set(qn("w:val"), "clear")
    cell._tc.get_or_add_tcPr().append(s)

def table(headers, rows):
    t = doc.add_table(rows=1+len(rows), cols=len(headers))
    t.style = "Table Grid"
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    for i, ht in enumerate(headers):
        c = t.rows[0].cells[i]
        c.text = ht
        for run in c.paragraphs[0].runs:
            sf(run, bold=True, size=9, color=BLUE)
        shd_cell(c, "EEF3F9")
    for ri, row in enumerate(rows):
        tr = t.rows[ri+1]
        for ci, val in enumerate(row):
            c = tr.cells[ci]
            c.text = str(val)
            for run in c.paragraphs[0].runs:
                sf(run, size=9, color=DARK)
        if ri % 2 == 1:
            for c in tr.cells:
                shd_cell(c, "F7F9FC")
    return t

def bullet(text, size=10):
    p = doc.add_paragraph(style="List Bullet")
    r = p.add_run(text)
    sf(r, size=size, color=DARK)
    return p

